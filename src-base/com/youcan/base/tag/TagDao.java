package com.youcan.base.tag;

public class TagDao {
	
	private int id; // 标签ID
	private String name; // 标签名称

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
