package com.youcan.base.tag;

public class TagMapDao {
	
	private int tagId; // 标签ID
	private int vid; // 视频ID

	public int getTagId() {
		return tagId;
	}

	public void setTagId(int tagId) {
		this.tagId = tagId;
	}

	public int getVid() {
		return vid;
	}

	public void setVid(int vid) {
		this.vid = vid;
	}

}
