package com.youcan.base.module;

public class ModuleDao {

	private int id; // 模块ID
	private String name; // 模块名称

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
