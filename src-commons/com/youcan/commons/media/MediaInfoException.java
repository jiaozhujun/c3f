package com.youcan.commons.media;

public class MediaInfoException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 5814552378742112199L;

	public MediaInfoException() {
		super();
	}
	
	public MediaInfoException(String message) {
		super(message);
	}
	
	public MediaInfoException(String message, Throwable cause) {
		super(message, cause);
	}
}
